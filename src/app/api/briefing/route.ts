export async function POST(request: NextRequest) {
  try {
    const body = await request.json();

    // Validar datos
    const validatedData = briefingFormServerSchema.parse(body);

    // Metadata de la request
    const clientIP =
      request.headers.get("x-forwarded-for") ||
      request.headers.get("x-real-ip") ||
      "unknown";
    const referer = request.headers.get("referer") || "direct";

    const briefingData = {
      ...validatedData,
      metadata: {
        ip: clientIP,
        referer,
        timestamp: new Date().toISOString(),
        userAgent: validatedData.userAgent || "unknown"
      }
    };

    // Enviar email
    const emailResult = await sendBriefingEmail(briefingData);

    if (!emailResult.success) {
      console.error("‚ùå Error enviando email:", emailResult.error);
      throw new Error(emailResult.error || "Error enviando email");
    }

    return NextResponse.json({
      success: true,
      message: "Briefing recibido correctamente",
      timestamp: new Date().toISOString()
    });
  } catch (error) {
    console.error("‚ùå Error en formulario de briefing:", error);

    if (error instanceof z.ZodError) {
      return NextResponse.json(
        {
          error: "Datos del formulario inv√°lidos",
          details: error.issues
        },
        { status: 400 }
      );
    }

    return NextResponse.json(
      {
        error: "Error interno del servidor",
        message: "No se pudo procesar el briefing. Por favor, int√©ntalo de nuevo."
      },
      { status: 500 }
    );
  }
}

async function sendBriefingEmail(
  briefingData: z.infer<typeof briefingFormServerSchema> & {
    metadata: {
      ip: string;
      referer: string;
      timestamp: string;
      userAgent: string;
    };
  }
): Promise<EmailResponse> {
  try {
    const emailContent = {
      from: process.env.RESEND_FROM_EMAIL || "contacto@webcode.es",
      to: process.env.RESEND_TO_EMAIL || "info@webcode.es",
      subject: `üìã Nuevo Briefing: ${briefingData.nombre} - ${briefingData.tipoProyecto}`,
      html: generateBriefingEmailTemplate(briefingData),
      text: generateBriefingPlainTextEmail(briefingData),
      replyTo: briefingData.email
    };

    const resend = await getResend();
    const result = await resend.emails.send(emailContent);

    if (result.error) {
      return { success: false, error: result.error.message };
    }

    return { success: true, id: result.data?.id };
  } catch (error) {
    console.error("‚ùå Error en sendBriefingEmail:", error);
    return {
      success: false,
      error: error instanceof Error ? error.message : "Error desconocido"
    };
  }
}

